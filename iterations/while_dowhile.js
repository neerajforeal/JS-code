let index = 0;
while (index <= 10) {
  console.log(`value of index is ${index}`);
  index = index + 2;
}

// while loop with arrays
let myArr = ["flash", "batman", "superman", "wonderwoman", "aquaman"];
let arr = 0;
while (arr < myArr.length) {
  console.log(myArr[arr]);
  arr++;
}

// do while loop
let score = 1;
do {
  console.log(`Score is ${score}`);
  score++;
} while (score <= 10);
